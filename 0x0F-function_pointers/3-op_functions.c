#include "3-calc.h"

/**
 * op_add - add two int
 * @a: the first int
 * @b: the second int
 *
 * Return: int
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub two int
 * @a: the first int
 * @b: the second int
 *
 * Return: int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mul two ints
 * @a: thr first int
 * @b: the second int
 *
 * Return: mul two int
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - div two int
 * @a: the first int
 * @b: the second int
 *
 * Return: int
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod two int
 * @a: first int
 * @b: second int
 *
 * Return: int
 */

int op_mod(int a, int b)
{
	return (a % b);
}
